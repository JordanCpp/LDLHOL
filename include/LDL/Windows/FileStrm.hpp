#ifndef LDL_Windows_FileStrm_hpp
#define LDL_Windows_FileStrm_hpp

class LDL_FileStream
{
public:
	enum
	{
		ModeOpen = 1
	};

	LDL_FileStream();
	~LDL_FileStream();
	bool Open(const char* path, size_t mode);
	void Close();
	size_t Read(void* dest, size_t bytes);
private:
	bool   _isOpen;
	HANDLE _file;
};

#ifdef LDL_IMPLEMENTATION
LDL_FileStream::LDL_FileStream() :
	_isOpen(false),
	_file(NULL)
{
}

LDL_FileStream::~LDL_FileStream()
{
	void Close();
}

bool LDL_FileStream::Open(const char* path, size_t mode)
{
	DWORD fileMode = 0;

	if (mode == ModeOpen)
	{
		fileMode = GENERIC_READ;
	}

	_file = CreateFile(path, fileMode, FILE_SHARE_READ, NULL, OPEN_EXISTING, FILE_ATTRIBUTE_NORMAL, NULL);

	if ((_file != INVALID_HANDLE_VALUE))
	{
		_isOpen = true;
	}

	return _isOpen;
}

void LDL_FileStream::Close()
{
	if (_isOpen)
	{
		_isOpen = false;
		CloseHandle(_file);
	}
}

size_t LDL_FileStream::Read(void* dest, size_t bytes)
{
	DWORD count = 0;

	BOOL result = ReadFile(_file, dest, (DWORD)bytes, &count, NULL);

	if (result != FALSE)
	{
	}
	
	return count;
}
#endif

#endif
